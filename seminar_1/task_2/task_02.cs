Console.WriteLine("Введите номер дня недели:");
int numDay = Convert.ToInt32(Console.ReadLine());

if(numDay == 1)  Console.WriteLine("Понедельник");
if(numDay == 2)  Console.WriteLine("Вторник");
if(numDay == 3)  Console.WriteLine("Среда");
if(numDay == 4)  Console.WriteLine("Четверг");
if(numDay == 5)  Console.WriteLine("Пятница");
if(numDay == 6)  Console.WriteLine("Суббота");
if(numDay == 7)  Console.WriteLine("Воскресенье");
